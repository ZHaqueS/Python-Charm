import math

pi = 3.14
x = 1
y = 2
z = 3

# To rounding number
print(round(pi))

# cealing the float number(next int number)
print(math.ceil(pi))

# To get the (int number from the float)
print(math.floor(pi))

# To print the absolute value of the number
print(abs(pi))

# To make the power of the number
print(pow(pi,2))

# To find the Square Root of the Number
print((math.sqrt(pi)))

# To find the Maximum number from the numbers
print(max(x,y,z))

# To find the minimum number from the numbers
print(min(x,y,z))